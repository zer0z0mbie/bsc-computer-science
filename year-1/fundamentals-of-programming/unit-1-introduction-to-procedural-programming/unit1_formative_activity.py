# Printing Hello World!:
print("Hello, World!")

# Asking for two numbers:
num1 = int(input("Enter a number: "))
num2 = int(input("Enter another number: "))

# Performing and display the results of addition, subtraction, multiplication, and division of these two numbers:
addition = num1 + num2
subtraction = num1 - num2
multiplication = num1 * num2
division = num1 // num2

print("Addition:", num1, "+", num2, "=", addition)
print("Subtraction:", num1, "-", num2, "=", subtraction)
print("Multiplication:", num1, "*", num2, "=", multiplication)
print("Division:", num1, "/", num2, "=", division)